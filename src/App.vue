<template>
  <div id="app">
    <h1>Employees</h1>
    <employee-table @edit:employee="editEmployee" @delete:employee="deleteEmployee" :employees="employees" />
    <employee-form  @add:employee="addEmployee" />
  </div>
</template>

<script>
import HelloWorld from './components/HelloWorld.vue'
import EmployeeTable from './components/EmployeeTable.vue'
import EmployeeForm from './components/EmployeeForm.vue'

export default {
  name: 'app',
  components: {
    HelloWorld,
    EmployeeTable,
    EmployeeForm
  },
  data() {
    return {
        employees: []
    }
  },
  methods: {
    async addEmployee(employee) {
      try {
        const response = await fetch('https://jsonplaceholder.typicode.com/users', {
          method: 'POST',
          body: JSON.stringify(employee),
          headers: { 'Content-type': 'application/json; charset=UTF-8'}
        })
        const data = await response.json();
        this.employees = [...this.employees, data]
      } catch (error) {
        console.log(error)
      }
    },
    deleteEmployee (employee_id) {
      this.employees = this.employees.filter((employee) => {
        return employee.id !== employee_id;
      });
    },
    async getEmployees() {
      try {
        const response = await fetch('https://jsonplaceholder.typicode.com/users')
        const data = await response.json();
        this.employees = data
      } catch (error) {
        console.error(error)
      }
    },
    async editEmployee(employee_id, updatedEmployee) {
      try {
        const response = await fetch(`https://jsonplaceholder.typicode.com/users/${employee_id}`, {
          method: 'PUT',
          body: JSON.stringify(updatedEmployee)
        })
        const data = await response.json();
        this.employees = this.employees.map(employee => (employee.id === employee_id ? updatedEmployee : employee))
      } catch (error) {
          console.log("error", error);
      }
    } ,
    async deleteEmployee(id) {
      try {
        const response = await fetch(`https://jsonplaceholder.typicode.com/users/${id}`, {
          method: 'DELETE'
        })
        this.employees = this.employees.filter(employee => employee.id !== id);
      } catch (error) {
          console.log("error", error);
      }
    }
  },
  mounted() {
    this.getEmployees()
  }
}
</script>

